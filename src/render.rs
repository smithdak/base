pub fn enforcement(gate: &Gate, target: Target) -> &'static str {
    match (gate.kind, target) {
        (GateKind::StandingDenial, Target::Claude | Target::Codex | Target::Copilot)
            if gate.id == "never-push-default-branch" =>
        {
            "native-hook"
        }
        (GateKind::StandingDenial, _) => "advisory",
        (GateKind::StageApproval, Target::Claude | Target::Codex | Target::Copilot)
            if gate.satisfied_by.is_some() =>
        {
            // Pending verdicts are mechanically blocked. Routing a denial to
            // an aborted outcome is still a behavioral pipeline contract.
            "hybrid-hook"
        }
        (GateKind::StageApproval, Target::Claude | Target::Codex | Target::Copilot) => "assisted",
    }
}

pub fn policy_fidelity(policy: &Policy, target: Target) -> &'static str {
    match (target, policy.meta.event) {
        // Codex exposes turn `Stop`, not a session-end event. Mapping it would
        // change the lifecycle contract, so Base keeps this policy assisted.
        (Target::Codex, crate::canon::PolicyEvent::SessionEnd) => "assisted",
        (
            Target::Codex,
            crate::canon::PolicyEvent::PreToolUse | crate::canon::PolicyEvent::PostToolUse,
        ) if policy.meta.match_tools.is_empty() => "partial-hook",
        (_, crate::canon::PolicyEvent::PreToolUse | crate::canon::PolicyEvent::PostToolUse)
            if !policy
                .meta
                .match_tools
                .iter()
                .all(|matcher| target_tool_matcher_supported(matcher, target)) =>
        {
            "partial-hook"
        }
        _ => "native-hook",
    }
}

fn target_tool_matcher_supported(matcher: &str, target: Target) -> bool {
    if matcher.starts_with("mcp__") {
        return match target {
            Target::Claude | Target::Codex => true,
            // Copilot sanitizes the current built-in GitHub MCP server to
            // `github-mcp-server-*`. Other server names need an explicit
            // adapter mapping before Base can claim full matcher fidelity.
            Target::Copilot => matcher.starts_with("mcp__github__"),
        };
    }
    match target {
        Target::Codex => matches!(matcher, "Bash" | "apply_patch"),
        Target::Claude | Target::Copilot => matches!(
            matcher,
            "Bash"
                | "Read"
                | "Write"
                | "Edit"
                | "NotebookEdit"
                | "Glob"
                | "Grep"
                | "WebFetch"
                | "WebSearch"
                | "Task"
                | "apply_patch"
        ),
    }
}


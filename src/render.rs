pub const GENERATED_HEADER: &str = "<!-- generated by base sync — DO NOT EDIT -->";
pub const GENERATED_MARKER: &str = "generated by base sync — DO NOT EDIT";

pub type RenderedFiles = BTreeMap<String, Vec<u8>>;

pub fn render(canon: &Canon, config: &Config) -> Result<RenderedFiles> {
    let mut files = BTreeMap::new();
    for target in &config.targets {
        match target {
            Target::Claude => render_claude(&mut files, canon, config)?,
            Target::Codex => render_codex(&mut files, canon, config)?,
            Target::Copilot => render_copilot(&mut files, canon, config)?,
        }
    }

    // Codex and Copilot both discover the open Agent Skills layout. Emitting it
    // once avoids duplicate generated ownership when both targets are enabled.
    if config.targets.contains(&Target::Codex) || config.targets.contains(&Target::Copilot) {
        for skill in repo_skills(canon) {
            render_skill_tree(&mut files, ".agents/skills", skill);
        }
        for pipeline in repo_pipelines(canon) {
            insert_text(
                &mut files,
                &format!(".agents/skills/{}/SKILL.md", pipeline.meta.id),
                render_pipeline(canon, config, pipeline, PipelineSurface::SharedAgentSkill),
            );
        }
    }
    Ok(files)
}

fn render_claude(files: &mut RenderedFiles, canon: &Canon, config: &Config) -> Result<()> {
    insert_text(
        files,
        "CLAUDE.md",
        render_instructions(canon, config, Target::Claude),
    );

    for agent in repo_agents(canon) {
        insert_text(
            files,
            &format!(".claude/agents/{}.md", agent.meta.id),
            render_claude_agent(agent),
        );
    }
    for skill in repo_skills(canon) {
        render_skill_tree(files, ".claude/skills", skill);
    }
    for pipeline in repo_pipelines(canon) {
        insert_text(
            files,
            &format!(".claude/skills/{}/SKILL.md", pipeline.meta.id),
            render_pipeline(canon, config, pipeline, PipelineSurface::Claude),
        );
    }

    if let Some(settings) = render_claude_settings(canon, config)? {
        files.insert(".claude/settings.json".to_owned(), settings.into_bytes());
    }
    Ok(())
}

fn render_claude_settings(canon: &Canon, config: &Config) -> Result<Option<String>> {
    let denial = has_default_branch_denial(config);
    let artifact_gates = has_artifact_gate(config);
    let mut hooks: BTreeMap<String, Vec<Value>> = BTreeMap::new();

    if denial || artifact_gates {
        let entry = |tools: &[&str]| {
            json!({
                "matcher": target_glob_matcher(tools.iter().copied(), Target::Claude),
                "hooks": [{
                    "type": "command",
                    "command": "base",
                    "args": [
                        "__hook", "pre-tool", "--target", "claude",
                        "--default-branch", config.default_branch
                    ],
                    "timeout": 10,
                    "statusMessage": "Checking base gates"
                }]
            })
        };
        let pre_tool = hooks.entry("PreToolUse".to_owned()).or_default();
        pre_tool.push(entry(&["Bash"]));
        if artifact_gates {
            pre_tool.push(entry(&["Edit", "Write", "NotebookEdit", "apply_patch"]));
        }
        pre_tool.push(entry(&["mcp__github__*"]));
    }

    for policy in repo_policies(canon) {
        let mut group = json!({
            "hooks": [{
                "type": "command",
                "command": "base",
                "args": ["__hook", "policy", policy.meta.id, "--target", "claude"],
                // Give the wrapper time to turn an inner timeout into the
                // policy's declared fail-open/fail-closed result.
                "timeout": policy.meta.timeout_seconds.saturating_add(2),
                "statusMessage": format!("Running base policy {}", policy.meta.id)
            }]
        });
        if !policy.meta.match_tools.is_empty() {
            group["matcher"] = json!(target_glob_matcher(
                policy.meta.match_tools.iter(),
                Target::Claude
            ));
        }
        hooks
            .entry(policy.meta.event.claude_name().to_owned())
            .or_default()
            .push(group);
    }

    if hooks.is_empty() && !denial {
        return Ok(None);
    }
    let mut settings = Map::new();
    settings.insert(
        "$schema".to_owned(),
        json!("https://json.schemastore.org/claude-code-settings.json"),
    );
    settings.insert("hooks".to_owned(), serde_json::to_value(hooks)?);
    if denial {
        settings.insert(
            "permissions".to_owned(),
            json!({
                "deny": [
                    format!("Bash(git push * {}*)", config.default_branch),
                    format!("Bash(git push * HEAD:{0}*)", config.default_branch)
                ]
            }),
        );
    }
    let mut source =
        serde_json::to_string_pretty(&settings).context("cannot serialize Claude settings")?;
    source.push('\n');
    Ok(Some(source))
}

fn render_codex(files: &mut RenderedFiles, canon: &Canon, config: &Config) -> Result<()> {
    insert_text(
        files,
        "AGENTS.md",
        render_instructions(canon, config, Target::Codex),
    );
    for agent in repo_agents(canon) {
        insert_text(
            files,
            &format!(".codex/agents/{}.toml", agent.meta.id),
            render_codex_agent(agent)?,
        );
    }
    if let Some(hooks) = render_codex_hooks(canon, config)? {
        files.insert(".codex/hooks.json".to_owned(), hooks.into_bytes());
    }

    if has_default_branch_denial(config) {
        let branch = starlark_string(&config.default_branch);
        let rules = format!(
            r#"# {GENERATED_MARKER}

prefix_rule(
    pattern = ["git", "push", "origin", "{branch}"],
    decision = "forbidden",
    justification = "base standing denial: push a feature branch and open a review instead",
    match = ["git push origin {branch}"],
)

prefix_rule(
    pattern = ["git", "push", "origin", "HEAD:{branch}"],
    decision = "forbidden",
    justification = "base standing denial: do not push HEAD to the default branch",
    match = ["git push origin HEAD:{branch}"],
)
"#
        );
        files.insert(".codex/rules/base.rules".to_owned(), rules.into_bytes());
    }
    Ok(())
}

fn render_codex_hooks(canon: &Canon, config: &Config) -> Result<Option<String>> {
    let denial = has_default_branch_denial(config);
    let artifact_gates = has_artifact_gate(config);
    let mut hooks: BTreeMap<String, Vec<Value>> = BTreeMap::new();

    if denial || artifact_gates {
        let args = vec![
            "__hook".to_owned(),
            "pre-tool".to_owned(),
            "--target".to_owned(),
            "codex".to_owned(),
            "--default-branch".to_owned(),
            config.default_branch.clone(),
        ];
        let pre_tool = hooks.entry("PreToolUse".to_owned()).or_default();
        pre_tool.push(codex_command(
            &args,
            10,
            Some(&target_glob_matcher(["Bash"], Target::Codex)),
            true,
            config.requires_base.as_deref(),
        ));
        if artifact_gates {
            pre_tool.push(codex_command(
                &args,
                10,
                Some(&target_glob_matcher(
                    ["Edit", "Write", "NotebookEdit", "apply_patch"],
                    Target::Codex,
                )),
                true,
                config.requires_base.as_deref(),
            ));
        }
        pre_tool.push(codex_command(
            &args,
            10,
            Some(&target_glob_matcher(["mcp__github__*"], Target::Codex)),
            true,
            config.requires_base.as_deref(),
        ));
    }

    for policy in repo_policies(canon) {
        let Some(event) = codex_policy_event(policy) else {
            continue;
        };
        let args = vec![
            "__hook".to_owned(),
            "policy".to_owned(),
            policy.meta.id.clone(),
            "--target".to_owned(),
            "codex".to_owned(),
        ];
        let matcher = (!policy.meta.match_tools.is_empty())
            .then(|| target_glob_matcher(policy.meta.match_tools.iter(), Target::Codex));
        hooks
            .entry(event.to_owned())
            .or_default()
            .push(codex_command(
                &args,
                policy.meta.timeout_seconds.saturating_add(2),
                matcher.as_deref(),
                policy.meta.mode == PolicyMode::Guard && policy.meta.fail_closed,
                config.requires_base.as_deref(),
            ));
    }
    if hooks.is_empty() {
        return Ok(None);
    }
    let root = json!({
        "description": "Generated Base lifecycle policies for this repository.",
        "hooks": hooks
    });
    let mut source = serde_json::to_string_pretty(&root).context("cannot serialize Codex hooks")?;
    source.push('\n');
    Ok(Some(source))
}

fn codex_command(
    args: &[String],
    timeout: u64,
    matcher: Option<&str>,
    fail_closed: bool,
    requirement: Option<&str>,
) -> Value {
    let (command, command_windows) = portable_hook_commands(
        args,
        fail_closed,
        Target::Codex,
        "Base hook runtime is missing or incompatible",
        requirement,
    );
    let mut entry = json!({
        "hooks": [{
            "type": "command",
            "command": command,
            "commandWindows": command_windows,
            "timeout": timeout,
            "statusMessage": "Running Base repository policy"
        }]
    });
    if let Some(matcher) = matcher {
        entry["matcher"] = json!(matcher);
    }
    entry
}

#[derive(Serialize)]
struct CodexAgentFile<'a> {
    name: &'a str,
    description: &'a str,
    #[serde(skip_serializing_if = "Option::is_none")]
    sandbox_mode: Option<&'static str>,
    developer_instructions: String,
}

fn render_codex_agent(agent: &Agent) -> Result<String> {
    let mut instructions = agent.body.trim().to_owned();
    if !agent.meta.skills.is_empty() {
        instructions.push_str("\n\nRequired project skills: ");
        instructions.push_str(
            &agent
                .meta
                .skills
                .iter()
                .map(|id| format!("${id}"))
                .collect::<Vec<_>>()
                .join(", "),
        );
        instructions.push('.');
    }
    let file = CodexAgentFile {
        name: &agent.meta.id,
        description: &agent.meta.description,
        sandbox_mode: match agent.meta.access {
            AgentAccess::Inherit => None,
            AgentAccess::ReadOnly => Some("read-only"),
            AgentAccess::WorkspaceWrite => Some("workspace-write"),
        },
        developer_instructions: instructions,
    };
    let mut source = format!("# {GENERATED_MARKER}\n\n");
    source.push_str(&toml::to_string_pretty(&file).context("cannot serialize Codex agent")?);
    Ok(source)
}

fn render_copilot(files: &mut RenderedFiles, canon: &Canon, config: &Config) -> Result<()> {
    insert_text(
        files,
        ".github/copilot-instructions.md",
        render_instructions(canon, config, Target::Copilot),
    );
    for agent in repo_agents(canon) {
        insert_text(
            files,
            &format!(".github/agents/{}.agent.md", agent.meta.id),
            render_copilot_agent(agent),
        );
    }
    for pipeline in repo_pipelines(canon) {
        insert_text(
            files,
            &format!(".github/prompts/{}.prompt.md", pipeline.meta.id),
            render_pipeline(canon, config, pipeline, PipelineSurface::CopilotPrompt),
        );
    }
    if let Some(hooks) = render_copilot_hooks(canon, config)? {
        files.insert(".github/hooks/base.json".to_owned(), hooks.into_bytes());
    }
    Ok(())
}

fn render_copilot_agent(agent: &Agent) -> String {
    let mut output = String::new();
    line(&mut output, "---");
    line(&mut output, &format!("name: {}", agent.meta.id));
    line(
        &mut output,
        &format!("description: {}", yaml_scalar(&agent.meta.description)),
    );
    if agent.meta.access == AgentAccess::ReadOnly {
        line(&mut output, "tools: [read, search]");
    } else if !agent.meta.tools.is_empty() {
        line(
            &mut output,
            &format!("tools: {}", yaml_sequence(&agent.meta.tools)),
        );
    }
    line(&mut output, "---");
    line(&mut output, "");
    line(&mut output, GENERATED_HEADER);
    line(&mut output, "");
    line(&mut output, agent.body.trim());
    if !agent.meta.skills.is_empty() {
        line(&mut output, "");
        line(
            &mut output,
            &format!(
                "Load these project skills before acting: {}.",
                agent
                    .meta
                    .skills
                    .iter()
                    .map(|id| format!("`{id}`"))
                    .collect::<Vec<_>>()
                    .join(", ")
            ),
        );
    }
    if agent.meta.access != AgentAccess::Inherit {
        line(
            &mut output,
            &format!(
                "Access posture: `{}` (Copilot has no portable custom-agent sandbox field, so treat this as a required behavioral constraint).",
                agent_access(agent.meta.access)
            ),
        );
    }
    output
}

fn render_copilot_hooks(canon: &Canon, config: &Config) -> Result<Option<String>> {
    let denial = has_default_branch_denial(config);
    let artifact_gates = has_artifact_gate(config);
    let mut hooks: BTreeMap<String, Vec<Value>> = BTreeMap::new();

    if denial || artifact_gates {
        let args = vec![
            "__hook".to_owned(),
            "pre-tool".to_owned(),
            "--target".to_owned(),
            "copilot".to_owned(),
            "--default-branch".to_owned(),
            config.default_branch.clone(),
        ];
        let pre_tool = hooks.entry("PreToolUse".to_owned()).or_default();
        if denial && !artifact_gates {
            pre_tool.push(copilot_command(
                &args,
                10,
                Some(&target_glob_matcher(["Bash"], Target::Copilot)),
                true,
                config.requires_base.as_deref(),
            ));
        } else {
            pre_tool.push(copilot_command(
                &args,
                10,
                Some(&target_glob_matcher(
                    ["Bash", "Edit", "Write", "NotebookEdit"],
                    Target::Copilot,
                )),
                true,
                config.requires_base.as_deref(),
            ));
        }
        pre_tool.push(copilot_command(
            &args,
            10,
            Some(&target_glob_matcher(["mcp__github__*"], Target::Copilot)),
            true,
            config.requires_base.as_deref(),
        ));
    }

    for policy in repo_policies(canon) {
        let args = vec![
            "__hook".to_owned(),
            "policy".to_owned(),
            policy.meta.id.clone(),
            "--target".to_owned(),
            "copilot".to_owned(),
        ];
        let matcher = (!policy.meta.match_tools.is_empty())
            .then(|| target_glob_matcher(policy.meta.match_tools.iter(), Target::Copilot));
        hooks
            .entry(policy.meta.event.claude_name().to_owned())
            .or_default()
            .push(copilot_command(
                &args,
                policy.meta.timeout_seconds.saturating_add(2),
                matcher.as_deref(),
                policy.meta.mode == PolicyMode::Guard && policy.meta.fail_closed,
                config.requires_base.as_deref(),
            ));
    }
    if hooks.is_empty() {
        return Ok(None);
    }
    let root = json!({ "version": 1, "disableAllHooks": false, "hooks": hooks });
    let mut source =
        serde_json::to_string_pretty(&root).context("cannot serialize Copilot hooks")?;
    source.push('\n');
    Ok(Some(source))
}

fn copilot_command(
    args: &[String],
    timeout: u64,
    matcher: Option<&str>,
    fail_closed: bool,
    requirement: Option<&str>,
) -> Value {
    let (bash, powershell) = portable_hook_commands(
        args,
        fail_closed,
        Target::Copilot,
        "Base hook runtime is missing or incompatible",
        requirement,
    );
    let mut entry = json!({
        "type": "command",
        "bash": bash,
        "powershell": powershell,
        "timeoutSec": timeout
    });
    if let Some(matcher) = matcher {
        entry["matcher"] = json!(matcher);
    }
    entry
}

fn portable_hook_commands(
    args: &[String],
    fail_closed: bool,
    target: Target,
    reason: &str,
    requirement: Option<&str>,
) -> (String, String) {
    let bash_args = args
        .iter()
        .map(|arg| posix_shell_word(arg))
        .collect::<Vec<_>>()
        .join(" ");
    let powershell_args = args
        .iter()
        .map(|arg| powershell_literal(arg))
        .collect::<Vec<_>>()
        .join(" ");
    let mut capability_args = vec![
        "__hook".to_owned(),
        "capabilities".to_owned(),
        "--require-feature".to_owned(),
        args.get(1)
            .cloned()
            .unwrap_or_else(|| "pre-tool".to_owned()),
    ];
    if let Some(requirement) = requirement {
        capability_args.push("--require".to_owned());
        capability_args.push(requirement.to_owned());
    }
    let bash_capabilities = capability_args
        .iter()
        .map(|arg| posix_shell_word(arg))
        .collect::<Vec<_>>()
        .join(" ");
    let powershell_capabilities = capability_args
        .iter()
        .map(|arg| powershell_literal(arg))
        .collect::<Vec<_>>()
        .join(" ");
    let denial = denial_json(target, reason).to_string();
    let bash_denial = posix_shell_word(&denial);
    let powershell_denial = powershell_literal(&denial);
    if fail_closed {
        (
            format!(
                "if command -v base >/dev/null 2>&1 && base {bash_capabilities} >/dev/null 2>&1; then base {bash_args} || printf '%s\\n' {bash_denial}; else printf '%s\\n' {bash_denial}; fi"
            ),
            format!(
                "$b = Get-Command base -ErrorAction SilentlyContinue; if ($b) {{ & base {powershell_capabilities} *> $null; if ($LASTEXITCODE -eq 0) {{ & base {powershell_args}; if ($LASTEXITCODE -ne 0) {{ Write-Output {powershell_denial} }} }} else {{ Write-Output {powershell_denial} }} }} else {{ Write-Output {powershell_denial} }}"
            ),
        )
    } else {
        (
            format!(
                "if command -v base >/dev/null 2>&1 && base {bash_capabilities} >/dev/null 2>&1; then base {bash_args} || true; fi"
            ),
            format!(
                "$b = Get-Command base -ErrorAction SilentlyContinue; if ($b) {{ & base {powershell_capabilities} *> $null; if ($LASTEXITCODE -eq 0) {{ & base {powershell_args} }} }}; exit 0"
            ),
        )
    }
}

fn denial_json(target: Target, reason: &str) -> Value {
    match target {
        Target::Claude | Target::Codex => json!({
            "hookSpecificOutput": {
                "hookEventName": "PreToolUse",
                "permissionDecision": "deny",
                "permissionDecisionReason": reason
            }
        }),
        Target::Copilot => json!({
            "permissionDecision": "deny",
            "permissionDecisionReason": reason
        }),
    }
}

fn render_instructions(canon: &Canon, config: &Config, target: Target) -> String {
    let mut output = String::new();
    line(&mut output, GENERATED_HEADER);
    line(&mut output, "");
    line(&mut output, "# base agentic system");
    line(&mut output, "");
    line(
        &mut output,
        "Canonical definitions compile into this file. Change `.base/canon/` or installed pack inputs and run `base sync`; never edit generated output directly.",
    );

    if !config.packs.is_empty() {
        line(&mut output, "");
        line(&mut output, "## Composition");
        line(&mut output, "");
        line(
            &mut output,
            "Installed packs compose in declaration order; later packs override earlier IDs and `.base/canon/` is the final project overlay:",
        );
        for pack in &config.packs {
            line(
                &mut output,
                &format!(
                    "- `{}` version `{}` — {}",
                    pack.id, pack.version, pack.description
                ),
            );
        }
    }

    line(&mut output, "");
    line(&mut output, "## Rules");
    for rule in canon
        .rules
        .values()
        .filter(|rule| rule.source.repo_resident())
    {
        line(&mut output, "");
        line(&mut output, &format!("### {}", title(&rule.meta.id)));
        line(&mut output, "");
        line(&mut output, rule.body.trim());
    }

    line(&mut output, "");
    line(&mut output, "## Agents");
    line(&mut output, "");
    match target {
        Target::Claude => line(
            &mut output,
            "Native Claude Code subagents are available under `.claude/agents/`.",
        ),
        Target::Codex => line(
            &mut output,
            "Native Codex custom agents are available under `.codex/agents/`.",
        ),
        Target::Copilot => line(
            &mut output,
            "Native GitHub Copilot custom agents are available under `.github/agents/`.",
        ),
    }
    for agent in repo_agents(canon) {
        line(
            &mut output,
            &format!("- **{}** — {}", agent.meta.id, agent.meta.description),
        );
    }

    line(&mut output, "");
    line(&mut output, "## Skills");
    line(&mut output, "");
    for skill in repo_skills(canon) {
        let invocation = match target {
            Target::Claude => format!("`/{}`", skill.meta.id),
            Target::Codex => format!("`${}`", skill.meta.id),
            Target::Copilot => format!("agent skill `{}`", skill.meta.id),
        };
        line(
            &mut output,
            &format!(
                "- {invocation} — {}",
                skill.meta.description.trim_end_matches('.')
            ),
        );
    }

    line(&mut output, "");
    line(&mut output, "## Pipelines");
    line(&mut output, "");
    for pipeline in repo_pipelines(canon) {
        let invocation = match target {
            Target::Claude => format!("`/{}`", pipeline.meta.id),
            Target::Codex => format!("`${}`", pipeline.meta.id),
            Target::Copilot => format!(
                "Agent Skill `${}` or VS Code prompt `.github/prompts/{}.prompt.md`",
                pipeline.meta.id, pipeline.meta.id
            ),
        };
        line(
            &mut output,
            &format!(
                "- {invocation} — {}",
                pipeline.meta.description.trim_end_matches('.')
            ),
        );
    }

    line(&mut output, "");
    line(&mut output, "## Lifecycle policies");
    line(&mut output, "");
    if repo_policies(canon).next().is_none() {
        line(
            &mut output,
            "- No repository lifecycle policies are installed.",
        );
    } else {
        for policy in repo_policies(canon) {
            line(
                &mut output,
                &format!(
                    "- **{}** (`{}` / `{}` / `{}`): {}",
                    policy.meta.id,
                    policy_event(policy),
                    policy_mode(policy.meta.mode),
                    policy_fidelity(policy, target),
                    policy.meta.description
                ),
            );
            if policy_fidelity(policy, target) != "native-hook" {
                line(
                    &mut output,
                    &format!("  - Fallback: {}", policy.body.trim()),
                );
            }
        }
    }

    line(&mut output, "");
    line(&mut output, "## Verifiers");
    line(&mut output, "");
    if !canon
        .verifiers
        .values()
        .any(|suite| suite.source.repo_resident())
    {
        line(&mut output, "- No repository verifier suites are defined.");
    } else {
        for suite in canon
            .verifiers
            .values()
            .filter(|suite| suite.source.repo_resident())
        {
            line(
                &mut output,
                &format!(
                    "- `base verify {}` — {}",
                    suite.meta.id, suite.meta.description
                ),
            );
        }
    }

    line(&mut output, "");
    line(&mut output, "## Gates");
    line(&mut output, "");
    for gate in &config.gates {
        let fidelity = enforcement(gate, target);
        line(
            &mut output,
            &format!("- **{}** (`{fidelity}`): {}", gate.id, gate.description),
        );
    }
    line(
        &mut output,
        &format!(
            "- The repository default branch is `{}`.",
            config.default_branch
        ),
    );

    line(&mut output, "");
    line(&mut output, "## State and knowledge");
    line(&mut output, "");
    line(
        &mut output,
        "- Current work: `base state show`; session context: `base state context`; durable handoff: `.base/state/handoff.md`.",
    );
    line(
        &mut output,
        "- Work items: `.base/work/` (statuses `todo|doing|review|done`; `done` requires a human `pass|fail` verdict); run artifacts: `.base/runs/`; append-only ledger: `.base/history.jsonl`.",
    );
    let knowledge: Vec<(&String, &Source)> = canon
        .knowledge
        .iter()
        .filter(|(_, entry)| entry.source.repo_resident())
        .map(|(path, entry)| (path, &entry.source))
        .collect();
    if knowledge.is_empty() {
        line(
            &mut output,
            "- No canonical knowledge entries are currently available.",
        );
    } else {
        line(&mut output, "- Load canonical knowledge on demand from:");
        for (path, source) in knowledge {
            let location = match &source.pack {
                Some(pack) => format!(".base/packs/{pack}/knowledge/{path}"),
                None => format!(".base/canon/knowledge/{path}"),
            };
            line(&mut output, &format!("  - `{location}`"));
        }
    }
    output
}


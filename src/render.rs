#[derive(Clone, Copy)]
enum PipelineSurface {
    Claude,
    SharedAgentSkill,
    CopilotPrompt,
}

fn render_pipeline(
    canon: &Canon,
    config: &Config,
    pipeline: &Pipeline,
    surface: PipelineSurface,
) -> String {
    let mut output = String::new();
    line(&mut output, "---");
    match surface {
        PipelineSurface::Claude => {
            line(&mut output, &format!("name: {}", pipeline.meta.id));
            line(
                &mut output,
                &format!("description: {}", yaml_scalar(&pipeline.meta.description)),
            );
            line(&mut output, "disable-model-invocation: true");
            line(&mut output, "argument-hint: <task>");
        }
        PipelineSurface::SharedAgentSkill => {
            line(&mut output, &format!("name: {}", pipeline.meta.id));
            line(
                &mut output,
                &format!(
                    "description: {}",
                    yaml_scalar(&format!(
                        "{} Use when the user explicitly invokes the {} pipeline.",
                        pipeline.meta.description, pipeline.meta.id
                    ))
                ),
            );
        }
        PipelineSurface::CopilotPrompt => {
            line(&mut output, "agent: 'agent'");
            line(
                &mut output,
                &format!("description: {}", yaml_scalar(&pipeline.meta.description)),
            );
        }
    }
    line(&mut output, "---");
    line(&mut output, "");
    line(&mut output, GENERATED_HEADER);
    line(&mut output, "");
    line(
        &mut output,
        &format!("# {} pipeline", title(&pipeline.meta.id)),
    );
    line(&mut output, "");
    line(&mut output, pipeline.body.trim());
    line(&mut output, "");
    match surface {
        PipelineSurface::Claude => line(&mut output, "Task: $ARGUMENTS"),
        PipelineSurface::SharedAgentSkill => line(
            &mut output,
            "Use the task supplied alongside this explicitly invoked skill.",
        ),
        PipelineSurface::CopilotPrompt => line(
            &mut output,
            "Use the user's current request and attached context as the task.",
        ),
    }
    let harness = match surface {
        PipelineSurface::Claude => "Record `harness` as `claude` in the run ledger.",
        PipelineSurface::SharedAgentSkill => {
            "Record the actual runtime, `codex` or `copilot`, as `harness` in the run ledger."
        }
        PipelineSurface::CopilotPrompt => "Record `harness` as `copilot` in the run ledger.",
    };
    line(&mut output, harness);

    for (index, stage_ref) in pipeline.meta.stages.iter().enumerate() {
        let stage = canon
            .stages
            .get(&stage_ref.use_stage)
            .expect("canon was validated before rendering");
        line(&mut output, "");
        line(
            &mut output,
            &format!("## {}. {}", index + 1, title(&stage.meta.id)),
        );
        line(&mut output, "");
        line(&mut output, stage.body.trim());
        if let Some(agent) = &stage_ref.agent {
            line(&mut output, "");
            line(&mut output, &format!("### Assigned agent: {agent}"));
            line(&mut output, "");
            if stage_ref.independent_review {
                line(
                    &mut output,
                    &format!(
                        "Run this stage in a distinct `{agent}` agent context that did not implement the change. If this target cannot provide separate agent execution, report the review as assisted and do not claim independent maker/checker separation."
                    ),
                );
            } else {
                line(
                    &mut output,
                    &format!(
                        "Delegate this stage to the repository agent `{agent}` when native agent delegation is available; otherwise follow that agent's generated contract directly and report the fallback."
                    ),
                );
            }
        }
        if let Some(gate_id) = &stage_ref.gate {
            let gate = config.gate(gate_id).expect("canon gate was validated");
            line(&mut output, "");
            line(&mut output, &format!("### Gate: {}", gate.id));
            line(&mut output, "");
            let prose = if gate.kind == GateKind::StageApproval && gate.satisfied_by.is_some() {
                format!(
                    "STOP after completing this stage. {} Request the verdict by writing `{}` in the run folder describing what needs approval, then stop. The verdict is recorded from outside the session: `base approve <run-slug> {}` (or `--deny`) writes `{}` — a standing directive counts only when recorded that way, with `--note` citing it. Never create the verdict artifact yourself. If the recorded verdict is `denied`, skip to `record` with outcome `aborted`.",
                    gate.description,
                    gate.request_path(),
                    gate.id,
                    gate.approval_path()
                )
            } else {
                format!(
                    "STOP after completing this stage. {} Do not begin the next stage until the user gives explicit approval in this conversation. If approval is denied or unavailable, skip to `record` with outcome `aborted`.",
                    gate.description
                )
            };
            line(&mut output, &prose);
        }
        if let Some(verifier) = &stage_ref.verifier {
            line(&mut output, "");
            line(&mut output, &format!("### Verifier: {verifier}"));
            line(&mut output, "");
            line(
                &mut output,
                &format!(
                    "Run `base verify {verifier} --run <run-slug>`. A `fail` or `inconclusive` verdict is not passing evidence and must be reported before proceeding."
                ),
            );
        }
    }
    output
}


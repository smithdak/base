/// Compile Base's portable full-name glob semantics to an explicitly anchored
/// regular expression. Host regex syntax is never accepted as canon.
fn glob_matcher<I, S>(globs: I) -> String
where
    I: IntoIterator<Item = S>,
    S: AsRef<str>,
{
    let alternatives = globs
        .into_iter()
        .map(|glob| {
            let mut pattern = String::new();
            for character in glob.as_ref().chars() {
                match character {
                    '*' => pattern.push_str(".*"),
                    '?' => pattern.push('.'),
                    '\\' | '.' | '+' | '(' | ')' | '[' | ']' | '{' | '}' | '^' | '$' | '|' => {
                        pattern.push('\\');
                        pattern.push(character);
                    }
                    _ => pattern.push(character),
                }
            }
            pattern
        })
        .collect::<Vec<_>>()
        .join("|");
    format!("^(?:{alternatives})$")
}

fn target_glob_matcher<I, S>(globs: I, target: Target) -> String
where
    I: IntoIterator<Item = S>,
    S: AsRef<str>,
{
    let mut translated = Vec::<String>::new();
    for glob in globs {
        let glob = glob.as_ref();
        let mapped = match (target, glob) {
            (Target::Codex, "Edit" | "Write" | "NotebookEdit") => "apply_patch".to_owned(),
            (Target::Claude | Target::Copilot, "apply_patch") => "Edit".to_owned(),
            (Target::Copilot, "NotebookEdit") => "Edit".to_owned(),
            (Target::Copilot, value) if value.starts_with("mcp__github__") => format!(
                "github-mcp-server-{}",
                value.trim_start_matches("mcp__github__")
            ),
            _ => glob.to_owned(),
        };
        if !translated.contains(&mapped) {
            translated.push(mapped);
        }
    }
    glob_matcher(&translated)
}


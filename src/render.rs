fn insert_text(files: &mut RenderedFiles, path: &str, mut content: String) {
    if !content.ends_with('\n') {
        content.push('\n');
    }
    files.insert(path.to_owned(), content.into_bytes());
}

fn line(output: &mut String, value: &str) {
    output.push_str(value);
    output.push('\n');
}

fn title(id: &str) -> String {
    id.split('-')
        .filter(|part| !part.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                Some(first) => format!("{}{}", first.to_uppercase(), chars.as_str()),
                None => String::new(),
            }
        })
        .collect::<Vec<_>>()
        .join(" ")
}

fn yaml_scalar(value: &str) -> String {
    serde_json::to_string(value).expect("JSON string is valid YAML")
}

fn yaml_sequence(values: &[String]) -> String {
    serde_json::to_string(values).expect("JSON array is valid YAML")
}

fn starlark_string(value: &str) -> String {
    value.replace('\\', "\\\\").replace('"', "\\\"")
}

fn posix_shell_word(value: &str) -> String {
    format!("'{}'", value.replace('\'', "'\"'\"'"))
}

fn powershell_literal(value: &str) -> String {
    format!("'{}'", value.replace('\'', "''"))
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn title_formats_ids() {
        assert_eq!(title("plan-approval"), "Plan Approval");
    }

    #[test]
    fn enforcement_is_honest_per_cell() {
        let config = Config::default();
        let stage = config.gate("plan-approval").unwrap();
        let denial = config.gate("never-push-default-branch").unwrap();
        assert_eq!(enforcement(stage, Target::Claude), "hybrid-hook");
        assert_eq!(enforcement(stage, Target::Codex), "hybrid-hook");
        assert_eq!(enforcement(stage, Target::Copilot), "hybrid-hook");
        let prose_only = Gate {
            satisfied_by: None,
            ..stage.clone()
        };
        assert_eq!(enforcement(&prose_only, Target::Claude), "assisted");
        assert_eq!(enforcement(denial, Target::Claude), "native-hook");
        assert_eq!(enforcement(denial, Target::Codex), "native-hook");
        assert_eq!(enforcement(denial, Target::Copilot), "native-hook");
        let generic = Gate {
            id: "never-delete-production".to_owned(),
            kind: GateKind::StandingDenial,
            description: "Never delete production.".to_owned(),
            satisfied_by: None,
        };
        assert_eq!(enforcement(&generic, Target::Claude), "advisory");
    }

    #[test]
    fn shell_literals_do_not_interpolate() {
        assert_eq!(posix_shell_word("a'b"), "'a'\"'\"'b'");
        assert_eq!(powershell_literal("a'b"), "'a''b'");
    }

    #[test]
    fn portable_globs_compile_to_anchored_regex() {
        assert_eq!(
            glob_matcher(["Edit", "NotebookEdit", "mcp__github__*"]),
            "^(?:Edit|NotebookEdit|mcp__github__.*)$"
        );
        assert_eq!(
            glob_matcher(["tool.with+syntax?"]),
            "^(?:tool\\.with\\+syntax.)$"
        );
    }

    #[test]
    fn mutation_tool_aliases_translate_per_target() {
        assert_eq!(
            target_glob_matcher(["Edit", "NotebookEdit", "apply_patch"], Target::Codex),
            "^(?:apply_patch)$"
        );
        assert_eq!(
            target_glob_matcher(["apply_patch"], Target::Claude),
            "^(?:Edit)$"
        );
        assert_eq!(
            target_glob_matcher(["NotebookEdit", "apply_patch"], Target::Copilot),
            "^(?:Edit)$"
        );
        assert_eq!(
            target_glob_matcher(["mcp__github__*"], Target::Copilot),
            "^(?:github-mcp-server-.*)$"
        );
        assert!(target_tool_matcher_supported(
            "mcp__github__push_files",
            Target::Copilot
        ));
        assert!(!target_tool_matcher_supported(
            "mcp__custom__write",
            Target::Copilot
        ));
    }
}
